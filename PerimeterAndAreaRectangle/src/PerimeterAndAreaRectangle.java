
public class PerimeterAndAreaRectangle {

    
    public static void main(String[] args) {
        int length = 10;
        int width = 20;
        int perimeter = 2*(length + width);
        int area = width*length;
        
        System.out.println("Perimeter: " + perimeter);
        System.out.println("Area: " + area);
        
    }
    
}
